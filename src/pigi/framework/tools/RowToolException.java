package pigi.framework.tools;

/**
 * Exception thrown by RowTool
 * @author kgalecki
 *
 */
public class RowToolException extends Exception {

	/**
     * 
     */
    private static final long serialVersionUID = 826928211080886900L;

    public RowToolException() {
		super();
	}

	public RowToolException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public RowToolException(String arg0) {
		super(arg0);
	}

	public RowToolException(Throwable arg0) {
		super(arg0);
	}

}
