package pigi.framework.tools;

/**
 * Exception thrown by HTableFactory class
 * @author kgalecki
 *
 */
public class HTableFactoryException extends Exception {

	public HTableFactoryException() {
		super();
	}

	public HTableFactoryException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public HTableFactoryException(String arg0) {
		super(arg0);
	}

	public HTableFactoryException(Throwable arg0) {
		super(arg0);
	}

}
